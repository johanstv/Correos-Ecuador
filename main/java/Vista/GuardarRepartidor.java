    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Ruta rr = new Ruta(jTextField5.getText(), (double) jSpinner3.getValue());
            Vehiculo vr = new Vehiculo(jTextField6.getText(), jTextField7.getText(), rr);
            Trabajador r = new Repartidor((int) jSpinner2.getValue(), vr, jTextField2.getText(), jTextField3.getText(), jTextField1.getText(), (String) jComboBox1.getSelectedItem(), jTextField4.getText(), (int) jSpinner1.getValue());
            FileHandler fh = new FileHandler();
            //Validaciones
            MisValidaciones mv = new MisValidaciones();
            mv.validarId(jTextField1.getText());
            mv.validarId(jTextField5.getText());

            fh.guardarTrabajador(r);
            JOptionPane.showMessageDialog(rootPane, "Se guard√≥ correctamente");
            jSpinner1.setValue(0);
            jSpinner2.setValue(0);
            jSpinner3.setValue(0);
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            jTextField6.setText("");
            jTextField7.setText("");
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(rootPane, "Error al guardar");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

