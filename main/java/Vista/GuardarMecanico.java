    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Trabajador m = new Mecanico(vehiculos, jTextField2.getText(), jTextField3.getText(), jTextField1.getText(), (String) jComboBox1.getSelectedItem(), jTextField4.getText(), (int) jSpinner1.getValue());
            FileHandler fh = new FileHandler();
            //Validaciones
            MisValidaciones mv = new MisValidaciones();
            mv.validarId(jTextField1.getText());            
            
            fh.guardarTrabajador(m);
            vehiculos.clear();
            JOptionPane.showMessageDialog(rootPane, "Se guard√≥ correctamente");
            
            jSpinner1.setValue(0);
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(rootPane, "Error al guardar");
        } catch (Exception ex) {
           JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

